import math
import random

# 1 square root
print("Sqrt of 16:", math.sqrt(16))


# 2 power
print("2^5:", math.pow(2, 5))


# 3 factorial
print("5!:", math.factorial(5))


# 4 random number
print("Random number:", random.randint(1, 10))


# 5 random choice
items = ["apple", "banana", "orange"]
print("Random choice:", random.choice(items))