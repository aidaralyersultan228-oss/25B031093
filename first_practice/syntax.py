# Syntax examples (up to 5)

# 1) Indentation example
if 5 > 2:
    print("Five is greater than two!")

# 2) Variables
x = 5
y = "Hello"
print(x, y)

# 3) Case-sensitive
a = 10
A = 20
print(a, A)

# 4) Multi-line statement
total = (1 + 2 + 3 +
         4 + 5)
print(total)

# 5) Loop syntax
for i in range(3):
    print("i =", i)