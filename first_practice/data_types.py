# Data Types examples (up to 5)

# 1) Basic types
a = "text"      # str
b = 10          # int
c = 3.14        # float
d = True        # bool
print(type(a), type(b), type(c), type(d))

# 2) List
nums = [1, 2, 3]
print(nums, type(nums))

# 3) Tuple
t = (1, 2, 3)
print(t, type(t))

# 4) Set
s = {"a", "b", "c"}
print(s, type(s))

# 5) Dict
person = {"name": "Bob", "age": 18}
print(person, type(person))