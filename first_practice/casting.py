# Casting examples (up to 5)

# 1) int casting
x = int(1)
y = int(2.8)
z = int("3")
print(x, y, z)

# 2) float casting
a = float(1)
b = float(2.8)
c = float("3")
d = float("3.5")
print(a, b, c, d)

# 3) str casting
s1 = str("s1")
s2 = str(2)
s3 = str(3.0)
print(s1, s2, s3)

